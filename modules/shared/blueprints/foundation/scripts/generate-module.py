def emit_policy(m: Module, e: Entity, policy_spec: dict[str, Any]) -> str:
    """Emit `src/Policies/<Model>Policy.php` from one policies.json entry."""
    ns = f"{m.ns_module_root}\\Policies"
    abilities = policy_spec.get("abilities", []) or []
    method_lines = []
    for ability in abilities:
        action = ability.get("action")
        permission = ability.get("permission")
        scope = ability.get("scope", "")
        if not action:
            continue
        method_name = camel(action)
        doc = _php_docblock([
            _first_sentence(scope or f"{action} ability.", 200) or f"{action} ability.",
            "",
            "@return bool",
        ], indent="    ")
        if permission:
            body = f"        return $user->can('{permission}');"
        else:
            body = "        // TODO(gen): decide policy body — no permission binding declared on the blueprint.\n        return false;"
        method_lines.append(f"""
{doc}
    public function {method_name}(User $user, {e.class_name} $model): bool
    {{
{body}
    }}""")

    class_doc = _php_docblock([
        f"Authorization policy for {{@see \\{m.ns_module_root}\\Models\\{e.class_name}}}.",
        "",
        "Wired via `#[UsePolicy]` on the Model.",
        "",
        f"@category {m.studly_name}",
        "",
        "@since    0.1.0",
    ])

    # Alias the actor `User` import when the policy's model is ALSO named User
    # (e.g. identity/user's UserPolicy), otherwise PHP raises a name collision.
    actor_import_alias = "AuthUser" if e.class_name == "User" else "User"
    actor_type = actor_import_alias
    actor_import_line = (
        f"use Academorix\\User\\Models\\User as {actor_import_alias};"
        if actor_import_alias != "User"
        else "use Academorix\\User\\Models\\User;"
    )

    return f"""<?php

// {AUTOGEN_HEADER.format(tier=m.tier, name=m.name)}

declare(strict_types=1);

namespace {ns};

{actor_import_line}
use {m.ns_module_root}\\Models\\{e.class_name};

{class_doc}
final class {e.class_name}Policy
{{{"".join(method_lines).replace("User $user", f"{actor_type} $user")}
}}
"""


