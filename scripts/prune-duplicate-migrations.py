#!/usr/bin/env python3
"""Prune generator-emitted migrations that collide with hand-written ones.

Every migration filename follows `<Y-m-d_HMS>_create_<table>_table.php` OR
`<Y-m-d_HMS>_add_<column>_to_<table>_table.php`. When both a
hand-written migration (no AUTO-GENERATED header, older timestamp) AND
a generator-emitted migration (AUTO-GENERATED header, newer 2026_07_15_120000 timestamp)
exist for the same table, the generator's file is a duplicate and must
be deleted — Laravel would run BOTH `Schema::create` calls and the
second call fails on `SQLSTATE[42P07]: Duplicate table`.

This one-off script keeps every hand-written migration + deletes every
AUTO-GENERATED migration whose table already has a hand-written peer.
"""
import os
import re
from pathlib import Path

AUTOGEN_MARKER = "AUTO-GENERATED by generate-module.py"
CREATE_RE = re.compile(r'_create_([A-Za-z_]+)_table\.php$')

deleted = 0
scanned = 0
kept_pairs: list[tuple[str, str]] = []

for mig_dir in sorted(Path("backend-packages").glob("*/*/database/migrations")):
    if "vendor" in mig_dir.parts:
        continue
    # Group migrations by target table.
    per_table: dict[str, list[Path]] = {}
    for f in sorted(mig_dir.glob("*.php")):
        m = CREATE_RE.search(f.name)
        if not m:
            continue
        per_table.setdefault(m.group(1), []).append(f)

    for table, files in per_table.items():
        scanned += 1
        if len(files) < 2:
            continue
        # Two-or-more files claim to create the same table. Delete
        # every AUTO-GENERATED one; keep the hand-written one(s).
        hand_written = [f for f in files if AUTOGEN_MARKER not in f.read_text()]
        autogen = [f for f in files if AUTOGEN_MARKER in f.read_text()]
        if not hand_written or not autogen:
            # Either no hand-written OR no autogen — nothing to prune.
            continue
        # We have both. Delete the autogen files.
        for f in autogen:
            print(f"delete: {f}")
            f.unlink()
            deleted += 1
        for f in hand_written:
            kept_pairs.append((str(mig_dir), f.name))

print(f"\nScanned tables:   {scanned}")
print(f"Deleted files:    {deleted}")
print(f"Kept hand-written migrations: {len(kept_pairs)}")
