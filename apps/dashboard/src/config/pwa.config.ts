/**
 * @file pwa.config.ts
 * @module config/pwa.config
 *
 * @description
 * Single source of truth for **everything that lands in the Web App
 * Manifest** and **everything Workbox emits into the service worker**.
 * `vite.config.ts` consumes this file at build time; nothing else does.
 *
 * ## Why split this out of vite.config.ts
 *
 *  1. The manifest is data, not build logic. Keeping it in `vite.config`
 *     buried it under 200 lines of chunk-splitting minutiae.
 *  2. Arabic (and future) translations need a typed table to pull from —
 *     see {@link buildManifest} which composes the localized values from
 *     `i18n.config.ts` at build time.
 *  3. Desktop (Tauri) and future admin surfaces will want to read the
 *     same manifest values (theme_color, icon paths, shortcuts) without
 *     importing from `vite.config.ts` — which is not a runtime module.
 *  4. Tests can import + snapshot the manifest without spinning up Vite.
 *
 * ## What lives here
 *
 *  - {@link BRAND_COLORS}          — hex values referenced by the manifest,
 *                                    index.html theme-color, and vercel
 *                                    security headers. One place, three
 *                                    consumers.
 *  - {@link MANIFEST_ICONS}        — icon file registry.
 *  - {@link MANIFEST_SHORTCUTS}    — app shortcut registry with stable ids
 *                                    that i18n.config.ts translates.
 *  - {@link buildManifest}         — pure function returning the final
 *                                    manifest object with `translations`
 *                                    injected from PWA_MANIFEST_TRANSLATIONS.
 *  - {@link WORKBOX_OPTIONS}       — runtime caching + navigation fallback
 *                                    rules for the service worker.
 *  - {@link PWA_PLUGIN_OPTIONS}    — the full `VitePWA()` argument.
 *
 * ## What does NOT live here
 *
 *  - Vite build options (chunk splitting, minification, etc.) — those
 *    stay in `vite.config.ts`.
 *  - Icon rasterization — see `pwa-assets.config.ts` at the package
 *    root. Consumed by `@vite-pwa/assets-generator`, invoked manually.
 *  - Service-worker JS handlers (push, click) — those will live in
 *    `src/pwa/sw/` once we migrate from `generateSW` to `injectManifest`
 *    per notifications module § phase 2.
 *
 * ## Design notes
 *
 *  - Zero runtime deps. `import.meta.env` NOT used here — this file must
 *    remain safe for Node import from vite.config.ts.
 *  - Every string that ends up user-visible is either a fixed English
 *    value (matched at the OS install prompt) OR routed through
 *    `PWA_MANIFEST_TRANSLATIONS` in i18n.config.ts.
 */

