/**
 * Shape of the per-locale manifest translation the W3C draft (and
 * Chromium ≥ 100) recognizes. Kept as our own interface because the
 * `vite-plugin-pwa` types don't yet include it.
 */
interface ManifestTranslationEntry {
  name?: string;
  short_name?: string;
  description?: string;
  shortcuts?: Array<{
    url: string;
    name?: string;
    short_name?: string;
    description?: string;
  }>;
}

/**
 * The manifest shape we emit. The vite-plugin-pwa manifest type doesn't
 * know about `translations` yet (W3C draft) so we widen it here.
 */
type BuiltManifest = NonNullable<VitePWAOptions["manifest"]> & {
  translations?: Record<string, ManifestTranslationEntry>;
};

/**
 * Assembles the final Web App Manifest object.
 *
 * - English content lives inline (below) and is the fallback for any
 *   locale not present in `PWA_MANIFEST_TRANSLATIONS`.
 * - Every additional locale from `i18n.config.ts` is added to the
 *   `translations` field (W3C draft; Chromium 100+, Edge 100+, Samsung
 *   Internet 16+, Opera 86+, Android in general). Browsers without
 *   support silently ignore `translations` and use the primary strings.
 * - Shortcut translations are localised per-shortcut using the stable
 *   `id` field defined above; unknown ids fall back to the English name.
 *
 * The return type widens the plugin's manifest schema with our own
 * `translations` shape ({@link ManifestTranslationEntry}) so the
 * unknown-to-the-plugin field passes TypeScript.
 */
export function buildManifest(): BuiltManifest {
  const translations: Record<string, ManifestTranslationEntry> = {};

  for (const [locale, translation] of Object.entries(PWA_MANIFEST_TRANSLATIONS)) {
    if (!translation) continue;

    translations[locale] = {
      name: translation.name,
      short_name: translation.shortName,
      description: translation.description,
      shortcuts: MANIFEST_SHORTCUTS.map((shortcut) => {
        const localized = translation.shortcuts?.[shortcut.id];

        if (!localized) return { url: shortcut.url };

        return {
          url: shortcut.url,
          name: localized.name,
          short_name: localized.shortName,
          description: localized.description,
        };
      }),
    };
  }

  return {
    /** Full product name. Falls back here if `translations` unsupported. */
    name: "Academorix — Academy Operations",

    /** Short home-screen label — ≤ 12 chars is the sweet spot. */
    short_name: "Academorix",

    /** One-line pitch. Shown in some install prompts + store listings. */
    description:
      "Academorix — the operating system for modern academies. Manage athletes, teams, sessions, matches, payments, and safeguarding from one place.",

    /** Primary manifest locale. Everything else comes via `translations`. */
    lang: "en",

    /**
     * Primary manifest direction. Kept `"ltr"` because the plugin's
     * TypeScript type only permits `"rtl" | "ltr"`. Per-locale entries
     * under `translations` don't override direction in the current
     * manifest spec — instead, `lang` is what browsers use to pick
     * text shaping. The SPA still renders RTL at runtime by setting
     * `<html dir="rtl">` on the client based on the active locale.
     */
    dir: "ltr",

    /** No browser chrome — the "app" feel. */
    display: "standalone",

    /** Preferred alternates if `standalone` isn't supported. */
    display_override: ["window-controls-overlay", "standalone", "minimal-ui", "browser"],

    /** Vertical on phones, natural on tablets/desktops. */
    orientation: "any",

    /** Browser chrome tint + splash top strip. */
    theme_color: BRAND_COLORS.themeLight,

    /** Splash screen background. */
    background_color: BRAND_COLORS.background,

    /** SW controls every route under this scope. */
    scope: "/",

    /** Opens the workspace picker. `?source=pwa` gets stripped in-app. */
    start_url: "/?source=pwa",

    /** Store-listing category hints. */
    categories: ["business", "productivity", "sports"],

    /** Icon list — see {@link MANIFEST_ICONS}. Cloned to mutable array. */
    icons: MANIFEST_ICONS.map((icon) => ({ ...icon })),

    /**
     * Shortcut list — we strip our internal `id` field on the way out
     * because the manifest spec doesn't recognize it. The `id` still
     * matters here because translations reference shortcuts by id.
     *
     * We also spread the readonly `icons` tuple into a fresh array
     * because `VitePWAOptions["manifest"]` types `icons` as mutable.
     */
    shortcuts: MANIFEST_SHORTCUTS.map(({ id: _id, icons, ...rest }) => ({
      ...rest,
      icons: icons.map((icon) => ({ ...icon })),
    })),

    /**
     * `translations` — W3C manifest draft. One key per non-primary
     * locale. Each entry contains a subset of the parent manifest with
     * localized `name`, `short_name`, `description`, and per-shortcut
     * localized labels keyed by URL (browsers match on url, not on our
     * internal id).
     *
     * Browsers that don't yet support `translations`:
     *   - Firefox (as of 2026): ignore silently and show English.
     *   - Safari on iOS: ignore silently.
     *
     * We accept the graceful degradation — Arabic-first users on
     * unsupported browsers still get the app; they just see English on
     * the launcher tile until they open it, at which point the SPA's
     * runtime i18n takes over.
     */
    translations,
  };
}

