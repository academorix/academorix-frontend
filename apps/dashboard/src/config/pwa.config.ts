/**
 * Runtime caching + navigation fallback for the service worker. The
 * generator (`strategies: "generateSW"`) reads this and emits `sw.js`.
 *
 * Order matters — the first matching entry wins.
 */
export const WORKBOX_OPTIONS: VitePWAOptions["workbox"] = {
  /**
   * Precache glob. Covers every asset Vite emits. We do NOT precache
   * source maps (each can be > 1 MB) and we do NOT precache mock JSON
   * fixtures.
   */
  globPatterns: ["**/*.{js,css,html,ico,png,svg,webp,woff,woff2}"],
  globIgnores: ["**/data/**", "**/*.map"],

  /**
   * Every dynamic navigation falls back to /index.html — the SPA shell.
   * Denylisted paths never fall back so the service worker never
   * masks a real API/asset with the HTML shell.
   */
  navigateFallback: "/index.html",
  navigateFallbackDenylist: [
    /^\/api\//, // Backend API.
    /^\/sw\.js$/, // Service worker itself.
    /^\/workbox-.*\.js$/, // Workbox runtime chunks.
    /^\/manifest\.webmanifest$/, // Manifest — served as-is.
  ],

  runtimeCaching: [
    // -----------------------------------------------------------------
    // Backend API — NetworkFirst
    // -----------------------------------------------------------------
    {
      urlPattern: ({ url, sameOrigin }) => sameOrigin && url.pathname.startsWith("/api/"),
      handler: "NetworkFirst",
      method: "GET",
      options: {
        cacheName: "academorix-api-v1",
        networkTimeoutSeconds: 5,
        expiration: {
          maxEntries: 200,
          maxAgeSeconds: 60 * 60, // 1 hour
          purgeOnQuotaError: true,
        },
        cacheableResponse: { statuses: [0, 200] },
      },
    },
    // -----------------------------------------------------------------
    // Same-origin images — CacheFirst
    // -----------------------------------------------------------------
    {
      urlPattern: ({ request, sameOrigin }) => sameOrigin && request.destination === "image",
      handler: "CacheFirst",
      options: {
        cacheName: "academorix-images-v1",
        expiration: {
          maxEntries: 100,
          maxAgeSeconds: 60 * 60 * 24 * 30, // 30 days
          purgeOnQuotaError: true,
        },
        cacheableResponse: { statuses: [0, 200] },
      },
    },
    // -----------------------------------------------------------------
    // Google Fonts stylesheet — StaleWhileRevalidate
    // -----------------------------------------------------------------
    {
      urlPattern: ({ url }) => url.origin === "https://fonts.googleapis.com",
      handler: "StaleWhileRevalidate",
      options: {
        cacheName: "google-fonts-stylesheets-v1",
        expiration: { maxEntries: 10, maxAgeSeconds: 60 * 60 * 24 * 7 },
      },
    },
    // -----------------------------------------------------------------
    // Google Fonts files — CacheFirst
    // -----------------------------------------------------------------
    {
      urlPattern: ({ url }) => url.origin === "https://fonts.gstatic.com",
      handler: "CacheFirst",
      options: {
        cacheName: "google-fonts-webfonts-v1",
        expiration: { maxEntries: 30, maxAgeSeconds: 60 * 60 * 24 * 365 },
        cacheableResponse: { statuses: [0, 200] },
      },
    },
  ],

  cleanupOutdatedCaches: true,
  clientsClaim: true,
  skipWaiting: false,
  maximumFileSizeToCacheInBytes: 5 * 1024 * 1024,
  sourcemap: false,
};

