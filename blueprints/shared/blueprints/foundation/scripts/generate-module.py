#!/usr/bin/env python3
"""generate-module.py — blueprint-driven Laravel module package generator.

Sibling to `generate-sdk.py`. Reads the same module blueprint and emits the
**server-side** Laravel package under `backend-packages/<tier>/<module>/`:

    - composer.json / phpstan.neon / phpunit.xml / README.md
    - src/Providers/<Name>ServiceProvider.php
    - database/migrations/YYYY_MM_DD_HHMMSS_create_<table>_table.php
    - src/Contracts/Data/<Model>Interface.php         (TABLE + ATTR_* constants + #[Bind])
    - src/Models/<Model>.php                          (#[Table] + #[Fillable] + #[UseFactory] + #[UsePolicy])
    - src/Contracts/Repositories/<Model>RepositoryInterface.php
    - src/Repositories/<Model>Repository.php          (#[AsRepository] + #[UseModel])
    - src/Data/<Model>Data.php                        (server-side output DTO)
    - database/factories/<Model>Factory.php
    - database/seeders/<Model>Seeder.php              (dual-source catalogues only)
    - src/Policies/<Model>Policy.php                  (from policies.json)
    - src/Events/*.php                                (from events.json)
    - src/Actions/*.php                               (from module.json routes — one Action per CRUD op)
    - tests/{Feature,Unit}/*Test.php                  (Pest v4 scaffolds)

Wire SDK (Saloon / Spatie Data) is a separate package under `sdk/`.
Both packages share one `composer.json` per side, both consume the same
blueprint through `_blueprint.read_module()`.

Every emitted file carries the AUTO-GENERATED header so regeneration with
`--force` cleanly replaces without clobbering hand-tuned overrides
(same convention as `generate-sdk.py`).

Usage:
    python3 modules/shared/blueprints/foundation/scripts/generate-module.py \\
        <tier> <module> [--force] [--dry-run]

    # Regenerate all in-scope modules:
    bash scripts/gen-all-modules.sh
"""

