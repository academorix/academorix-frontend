def _faker_call(col: Column) -> str:
    """Return a PHP faker expression for a Column."""
    if col.is_enum and col.enum_values:
        vals = ", ".join(f"'{v}'" for v in col.enum_values if isinstance(v, str))
        return f"fake()->randomElement([{vals}])"
    name_lower = col.name.lower()
    if "email" in name_lower:
        return "fake()->safeEmail()"
    if "phone" in name_lower:
        return "fake()->phoneNumber()"
    if "url" in name_lower:
        return "fake()->url()"
    if name_lower.endswith("_at") or name_lower == "date":
        return "now()"
    if col.php_type == "bool":
        return "fake()->boolean()"
    if col.php_type == "int":
        return "fake()->numberBetween(1, 1000)"
    if col.php_type == "float":
        return "fake()->randomFloat(2, 0, 10000)"
    if col.php_type == "array":
        return "[]"
    if col.max_length and col.max_length <= 32:
        return f"fake()->word()"
    return "fake()->sentence(3)"


