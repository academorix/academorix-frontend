def emit_event(m: Module, event_spec: dict[str, Any]) -> tuple[str, str]:
    """Emit one event class from an events.json entry. Returns (path, body)."""
    ns = f"{m.ns_module_root}\\Events"
    name = event_spec.get("name") or "UnknownEvent"
    class_name = studly(name)
    trigger = event_spec.get("trigger", "")
    consumers = event_spec.get("consumers", []) or []
    payload = event_spec.get("payload", {}) or {}

    prop_lines = []
    ctor_doc_lines = []
    for key, kind in payload.items():
        prop_name = camel(key)
        php_type = _payload_type_to_php(kind)
        prop_lines.append(f"        public readonly {php_type} ${prop_name},")
        ctor_doc_lines.append(f"@param  {php_type}  ${prop_name}  {kind}")

    doc_lines = [
        f"{trigger}" if trigger else f"Domain event: {class_name}.",
        "",
    ]
    if consumers:
        doc_lines.append("## Consumers")
        doc_lines.append("")
        for c in consumers:
            doc_lines.append(f"- {c}")
        doc_lines.append("")
    doc_lines += [
        f"@category {m.studly_name}",
        "",
        "@since    0.1.0",
    ]

    ctor_doc = _php_docblock(ctor_doc_lines, indent="    ") if ctor_doc_lines else ""
    props_block = "\n".join(prop_lines) if prop_lines else ""

    # `#[AsEvent]` marks the class for discovery by `stackra/events`'s
    # `EventDiscovery` per ADR-0010. Without it, listeners registered via
    # `#[OnEvent(Class::class)]` cannot resolve the fully-qualified name
    # from a reflection scan — the class ships but nobody hears it fire.
    body = f"""<?php

// {AUTOGEN_HEADER.format(tier=m.tier, name=m.name)}

declare(strict_types=1);

namespace {ns};

use Stackra\\Events\\Attributes\\AsEvent;
use Illuminate\\Contracts\\Events\\ShouldDispatchAfterCommit;

{_php_docblock(doc_lines)}
#[AsEvent]
final readonly class {class_name} implements ShouldDispatchAfterCommit
{{
{ctor_doc}
    public function __construct(
{props_block}
    ) {{
    }}
}}
"""
    return f"src/Events/{class_name}.php", body


def _payload_type_to_php(kind: str) -> str:
    """Map an events.json payload type description to a PHP type hint."""
    k = str(kind).lower()
    if k.startswith("ulid") or k == "string":
        return "string"
    if k.startswith("iso") or "8601" in k:
        return "string"
    if "int" in k:
        return "int"
    if "bool" in k:
        return "bool"
    if "float" in k or "double" in k or "decimal" in k:
        return "float"
    if "array" in k or "object" in k or "jsonb" in k:
        return "array"
    if "|" in k:
        # Union like "'a'|'b'" — string.
        return "string"
    return "string"


