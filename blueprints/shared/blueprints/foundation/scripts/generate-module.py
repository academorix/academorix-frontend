def emit_model_interface(m: Module, e: Entity) -> str:
    """Emit `src/Contracts/Data/<Model>Interface.php` — table shape."""
    ns = f"{m.ns_module_root}\\Contracts\\Data"
    table = e.aggregate.replace("-", "_")
    attr_lines = []
    max_name_len = max(len(c.name.upper()) for c in e.columns) if e.columns else 4
    for col in e.columns:
        const_name = f"ATTR_{col.name.upper()}"
        padding = " " * (max_name_len - len(col.name.upper()) + 1)
        attr_lines.append(f"    public const string {const_name}{padding}= '{col.name}';")
    columns_block = "\n".join(attr_lines) if attr_lines else "    // No columns declared on the blueprint."

    class_doc = _php_docblock([
        f"Table shape for the `{table}` table.",
        "",
        _first_sentence(e.description, 200),
        "",
        f"@category {m.studly_name}",
        "",
        "@since    0.1.0",
    ])

    return f"""<?php

// {AUTOGEN_HEADER.format(tier=m.tier, name=m.name)}

declare(strict_types=1);

namespace {ns};

use Illuminate\\Container\\Attributes\\Bind;
use {m.ns_module_root}\\Models\\{e.class_name};

{class_doc}
#[Bind({e.class_name}::class)]
interface {e.class_name}Interface
{{
    /**
     * Table name.
     */
    public const string TABLE = '{table}';

    /**
     * Primary key column.
     */
    public const string PRIMARY_KEY = 'id';

    /**
     * Primary key column type — string (ULID with prefix `{e.key_prefix}`).
     */
    public const string KEY_TYPE = 'string';

    // ── Columns ───────────────────────────────────────────────

{columns_block}
}}
"""


