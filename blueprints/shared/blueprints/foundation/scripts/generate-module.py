def write_module(m: Module, out_root: Path, *, force: bool, dry_run: bool) -> tuple[int, int]:
    """Emit the complete server-side module package."""
    pkg_dir = out_root / m.module_pkg_path

    if pkg_dir.exists() and not force and not dry_run:
        raise SystemExit(
            f"{pkg_dir} already exists — use --force to overwrite, or --into to emit elsewhere"
        )

    files: list[tuple[str, str]] = []

    # Static top-level files.
    files.append(("composer.json", emit_composer_json(m)))
    files.append(("phpstan.neon", emit_phpstan_neon()))
    files.append(("phpunit.xml", emit_phpunit_xml()))
    files.append(("README.md", emit_readme(m)))

    # Service provider.
    files.append((
        f"src/Providers/{m.studly_name}ServiceProvider.php",
        emit_service_provider(m),
    ))

    # Migrations — one per entity. Use a shared base timestamp so relative
    # ordering matches entity discovery order. Skip emission when a
    # hand-written migration already creates the same table (real-impl
    # packages ship their own migrations at earlier timestamps; the
    # generator's peer would collide on `Schema::create`).
    _reset_migration_sequence()
    base_ts = datetime(2026, 7, 15, 12, 0, 0)
    existing_migrations = list(pkg_dir.glob("database/migrations/*.php")) if pkg_dir.is_dir() else []
    for e in m.entities:
        target_table = _table_name_from_entity(
            e,
            (json.loads((module_dir_path(m.tier, m.name) / "schemas" / f"{e.name}.schema.json").read_text()).get("x-database", {}) or {})
            if (module_dir_path(m.tier, m.name) / "schemas" / f"{e.name}.schema.json").is_file()
            else {},
        )
        # Detect an existing `_create_<table>_table.php` (any timestamp).
        collision_suffix = f"_create_{target_table}_table.php"
        collides_with_hand_written = any(
            existing.name.endswith(collision_suffix)
            and "AUTO-GENERATED by generate-module.py" not in existing.read_text()
            for existing in existing_migrations
        )
        if collides_with_hand_written:
            # Real-impl already ships this table's migration. Skip
            # generator emission so re-fanouts don't reintroduce
            # duplicate CREATE TABLE statements.
            continue
        ts_str = _next_migration_timestamp(base_ts)
        filename, body = emit_migration(m, e, ts_str)
        files.append((f"database/migrations/{filename}", body))

    # Per-entity: interface, model, repo interface, repo, data, factory.
    for e in m.entities:
        files.append((f"src/Contracts/Data/{e.class_name}Interface.php", emit_model_interface(m, e)))
        files.append((f"src/Models/{e.class_name}.php", emit_model(m, e)))
        files.append((f"src/Contracts/Repositories/{e.class_name}RepositoryInterface.php", emit_repository_interface(m, e)))
        files.append((f"src/Repositories/{e.class_name}Repository.php", emit_repository(m, e)))
        files.append((f"src/Data/{e.class_name}Data.php", emit_data_dto(m, e)))
        files.append((f"database/factories/{e.class_name}Factory.php", emit_factory(m, e)))

    # Policies from policies.json.
    module_dir = module_dir_path(m.tier, m.name)
    policies = _load_aux(module_dir, "policies.json").get("policies", []) or []
    entities_by_model = {e.class_name: e for e in m.entities}
    for pol in policies:
        model_name = pol.get("model")
        if not model_name:
            continue
        entity = entities_by_model.get(model_name)
        if entity is None:
            continue
        files.append((f"src/Policies/{entity.class_name}Policy.php", emit_policy(m, entity, pol)))

    # Permission enum + seeder from permissions.json.
    # Matches the `shared/activity` + `platform/application` exemplar shape:
    # a `<StudlyName>Permission` enum implementing `PermissionEnum` with a
    # `guard()` method, plus a six-line seeder using `SeedsPermissionEnum`.
    permissions_raw = _load_aux(module_dir, "permissions.json")
    permissions_list = permissions_raw.get("permissions", []) or []
    if permissions_list:
        enum_emitted = emit_permission_enum(m, permissions_list)
        if enum_emitted is not None:
            files.append(enum_emitted)
        seeder_emitted = emit_permission_seeder(m, permissions_list)
        if seeder_emitted is not None:
            files.append(seeder_emitted)

    # Events from events.json.
    events = _load_aux(module_dir, "events.json").get("events", []) or []
    seen_events: set[str] = set()
    for event in events:
        if not isinstance(event, dict):
            continue
        name = event.get("name")
        if not name or name in seen_events:
            continue
        seen_events.add(name)
        path, body = emit_event(m, event)
        files.append((path, body))

    # Actions from routes (audience-split, dedupe by class name).
    #
    # Routes whose aggregate doesn't match any of the module's real
    # entities (e.g. `business-types` in `platform/application` — the
    # BusinessType schema is a VO catalogue, not a database table, per
    # ADR-0018) mean the aggregate is hand-owned outside the generator's
    # emission scope. In that case the hand-tuned Actions live in a
    # domain folder (e.g. `Actions/BusinessTypes/`) and the auto-emitter
    # would just produce duplicate stubs under `Actions/Platform/` /
    # `Actions/Tenant/` that shadow the real code. Skip the emit
    # entirely — the RouteRegistrar picks up the hand-tuned Action's
    # `#[Post(...)]` / `#[Get(...)]` attribute directly.
    aggregate_to_entity = {e.aggregate: e for e in m.entities}
    aggregate_to_entity.update({e.name: e for e in m.entities})
    seen_actions: set[str] = set()
    for route in m.routes:
        if route.op not in ("custom", "create", "update", "delete", "list", "show"):
            continue
        # Only emit CRUD actions when a real entity backs the aggregate.
        # Custom-verb actions still go through the stub path so their
        # route registration surface stays complete.
        if route.op != "custom" and route.aggregate not in aggregate_to_entity:
            continue
        path, body = emit_action(m, route)
        if path in seen_actions:
            continue
        seen_actions.add(path)
        files.append((path, body))

    # Enums — one per closed-set column across every entity.
    for e in m.entities:
        for enum_path, enum_body in emit_enums(m, e):
            files.append((enum_path, enum_body))

    # Request DTOs — one per create/update route per entity.
    for e in m.entities:
        for op in ("create", "update"):
            has_op = any(
                r.op == op and r.aggregate == e.aggregate for r in m.routes
            )
            if not has_op:
                continue
            path, body = emit_request_data(m, e, op)
            files.append((path, body))

    # Exceptions from errors.json.
    errors_raw = _load_aux(module_dir, "errors.json")
    errors_list = errors_raw.get("errors") or errors_raw.get("classes") or []
    for err in errors_list:
        if not isinstance(err, dict):
            continue
        emitted = emit_exception(m, err)
        if emitted is None:
            continue
        files.append(emitted)

    # Console commands from module.json → contributes.commands.
    module_json_path = module_dir / "module.json"
    module_json = json.loads(module_json_path.read_text())
    contributes = module_json.get("contributes", {}) or {}
    for cmd_signature in contributes.get("commands", []) or []:
        if not isinstance(cmd_signature, str):
            continue
        files.append(emit_console_command(m, cmd_signature))

    # Jobs from module.json → contributes.jobs.
    for job_name in contributes.get("jobs", []) or []:
        if not isinstance(job_name, str):
            continue
        files.append(emit_job(m, job_name))

    # Services + service contracts from module.json → contributes.bindings.
    # Skip anything ending in `Repository` / `RepositoryInterface` — already
    # emitted per entity from schemas.
    for binding_name in contributes.get("bindings", []) or []:
        if not isinstance(binding_name, str):
            continue
        if binding_name.endswith("Repository") or binding_name.endswith("RepositoryInterface"):
            continue
        files.append(emit_service_interface(m, binding_name))
        files.append(emit_service_concrete(m, binding_name))

    # Traits (concerns) from module.json → contributes.traits.
    for trait_name in contributes.get("traits", []) or []:
        if not isinstance(trait_name, str):
            continue
        files.append(emit_trait(m, trait_name))

    # PHP attributes from module.json → contributes.attributes.
    for attr_name in contributes.get("attributes", []) or []:
        if not isinstance(attr_name, str):
            continue
        files.append(emit_php_attribute(m, attr_name))

    # #[AsSetting] classes from settings.json → groups[].
    # The composer.json PSR-4 autoload prefix is the ground truth for the
    # module's namespace root — real-impl packages carry tier-prefixed
    # namespaces the generator's `ns_module_root` does not compute. When the
    # composer.json is emitted by the generator it uses `ns_module_root`;
    # when the composer.json is hand-tuned it may declare a different prefix
    # (like `Stackra\Notifications\Mail\` for notifications-mail).
    # Either way, the settings namespace must match the autoload prefix so
    # PSR-4 resolves the emitted class.
    module_ns_override: str | None = None
    composer_path = pkg_dir / "composer.json"
    if composer_path.is_file():
        try:
            composer_data = json.loads(composer_path.read_text())
            psr4 = composer_data.get("autoload", {}).get("psr-4", {})
            for prefix in psr4:
                trimmed = prefix.rstrip("\\")
                # Prefer the shortest prefix (the root); skip nested keys
                # like `<Ns>\Database\Factories\`.
                if module_ns_override is None or len(trimmed) < len(module_ns_override):
                    if "Database" not in trimmed and "Tests" not in trimmed:
                        module_ns_override = trimmed
        except json.JSONDecodeError:
            pass

    settings_raw = _load_aux(module_dir, "settings.json")
    settings_groups_raw = settings_raw.get("groups") or settings_raw.get("settings") or []
    if isinstance(settings_groups_raw, list):
        for group in settings_groups_raw:
            if not isinstance(group, dict):
                continue
            emitted = emit_settings_group(m, group, ns_override=module_ns_override)
            if emitted is None:
                continue
            files.append(emitted)

    # Test scaffolds.
    files.append(("tests/Pest.php", emit_pest_bootstrap(m)))
    files.append(("tests/TestCase.php", emit_test_case(m)))
    files.append(("tests/Feature/.gitkeep", ""))
    files.append(("tests/Unit/.gitkeep", ""))
    for e in m.entities:
        files.append((f"tests/Unit/{e.class_name}Test.php", emit_unit_test_stub(m, e)))

    written = 0
    skipped = 0
    for rel, body in files:
        target = pkg_dir / rel
        if dry_run:
            print(f"  would write: {target.relative_to(out_root)}")
            written += 1
            continue
        target.parent.mkdir(parents=True, exist_ok=True)
        # Preserve hand-tuned files (no AUTO-GENERATED header) on --force.
        if target.exists() and force and target.name != ".gitkeep":
            existing = target.read_text()
            if "AUTO-GENERATED by generate-module.py" not in existing and existing.strip():
                skipped += 1
                continue
        target.write_text(body)
        written += 1
    return written, skipped


# ---------------------------------------------------------------------------
# Main.
# ---------------------------------------------------------------------------

def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("tier", help="Module tier (e.g. platform, sports, finance).")
    parser.add_argument("name", help="Module name (e.g. forms, registrations, tenants).")
    parser.add_argument("--force", action="store_true", help="Overwrite existing generated files.")
    parser.add_argument("--dry-run", action="store_true", help="Print intended writes; don't touch disk.")
    parser.add_argument(
        "--into",
        default=str(REPO_ROOT),
        help="Alternate output root (for round-trip diff testing).",
    )
    args = parser.parse_args(argv)

    module = read_module(args.tier, args.name)
    print(
        f"Loaded blueprint: tier={args.tier}, name={args.name}, "
        f"entities={len(module.entities)}, routes={len(module.routes)}"
    )

    out_root = Path(args.into)
    written, skipped = write_module(module, out_root, force=args.force, dry_run=args.dry_run)
    print(f"Wrote {written} file(s); skipped {skipped} hand-tuned file(s).")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
