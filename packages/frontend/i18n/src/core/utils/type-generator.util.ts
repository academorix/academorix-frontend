/**
 * @file type-generator.util.ts
 * @module @stackra/i18n/core/utils
 * @description Generates TypeScript type definitions from translation JSON files.
 *   Produces a `.d.ts` file with recursive key types for IDE autocomplete.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync, statSync } from "fs";
import { join, dirname, basename } from "path";

import type { TypeGeneratorOptions } from "../interfaces";

// ============================================================================
// Public API
// ============================================================================

/**
 * Generate TypeScript type definitions from translation JSON files.
 *
 * Reads all JSON files from the first locale directory (source of truth),
 * flattens nested keys into dot-paths, and generates a typed interface.
 *
 * @param options - Generator configuration
 * @returns Whether the file was written (false if unchanged)
 *
 * @example
 * ```typescript
 * generateI18nTypes({
 *   translationsPath: './src/i18n',
 *   outputPath: './src/@types/i18n.generated.d.ts',
 * });
 * ```
 */
export function generateI18nTypes(options: TypeGeneratorOptions): boolean {
  const { translationsPath, outputPath } = options;

  if (!existsSync(translationsPath)) {
    throw new Error(`[i18n] Translations path not found: ${translationsPath}`);
  }

  // Find all locale directories
  const localeDirs = readdirSync(translationsPath).filter((entry) =>
    statSync(join(translationsPath, entry)).isDirectory(),
  );

  if (localeDirs.length === 0) {
    throw new Error(`[i18n] No locale directories found in: ${translationsPath}`);
  }

  // Use the first locale as the source of truth for type generation
  const sourceLocale = localeDirs[0]!;
  const sourceDir = join(translationsPath, sourceLocale);

  // Collect all translation keys from JSON files
  const translations = collectTranslationKeys(sourceDir);

  // Generate the TypeScript content
  const content = generateTypeContent(translations, localeDirs);

  // Check if file already has the same content (skip write if unchanged)
  if (existsSync(outputPath)) {
    const existing = readFileSync(outputPath, "utf-8");
    if (existing === content) return false;
  }

  // Write the file
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, content, "utf-8");

  return true;
}

// ============================================================================
// Internal Helpers
// ============================================================================

/**
 * Recursively collect all translation keys from JSON files in a directory.
 */
function collectTranslationKeys(dir: string): Record<string, "string" | Record<string, unknown>> {
  const result: Record<string, any> = {};

  const files = readdirSync(dir).filter((f) => f.endsWith(".json"));

  for (const file of files) {
    const namespace = basename(file, ".json");
    const filePath = join(dir, file);
    const content = JSON.parse(readFileSync(filePath, "utf-8"));

    result[namespace] = content;
  }

  return result;
}

/**
 * Generate the TypeScript type file content.
 */
function generateTypeContent(translations: Record<string, unknown>, locales: string[]): string {
  const lines: string[] = [
    "/* eslint-disable */",
    "/* prettier-ignore */",
    "// Auto-generated by @stackra/i18n type generator.",
    "// DO NOT EDIT MANUALLY.",
    "",
    `export type SupportedLocale = ${locales.map((l) => `'${l}'`).join(" | ")};`,
    "",
    "export interface I18nTranslations {",
  ];

  generateInterface(translations, lines, 1);

  lines.push("}");
  lines.push("");
  lines.push("export type I18nKey = FlattenKeys<I18nTranslations>;");
  lines.push("");
  lines.push("// Utility: flatten nested keys into dot-separated paths");
  lines.push('type FlattenKeys<T, Prefix extends string = ""> = T extends Record<string, unknown>');
  lines.push("  ? { [K in keyof T & string]: T[K] extends Record<string, unknown>");
  lines.push("    ? FlattenKeys<T[K], `${Prefix}${K}.`>");
  lines.push("    : `${Prefix}${K}` }[keyof T & string]");
  lines.push("  : never;");
  lines.push("");

  return lines.join("\n");
}

/**
 * Generate interface fields from a nested object.
 */
function generateInterface(obj: Record<string, unknown>, lines: string[], depth: number): void {
  const indent = "  ".repeat(depth);

  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === "object" && value !== null && !Array.isArray(value)) {
      lines.push(`${indent}${safeKey(key)}: {`);
      generateInterface(value as Record<string, unknown>, lines, depth + 1);
      lines.push(`${indent}};`);
    } else {
      lines.push(`${indent}${safeKey(key)}: string;`);
    }
  }
}

/**
 * Ensure a key is a valid TS identifier or wrap in quotes.
 */
function safeKey(key: string): string {
  if (/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)) return key;
  return `'${key}'`;
}
