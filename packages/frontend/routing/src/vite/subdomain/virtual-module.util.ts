/**
 * @file virtual-module.util.ts
 * @module @stackra/routing/vite/subdomain
 * @description Virtual-module identifier + source generator for the
 *   dev subdomain bridge (per PLAN v3.3).
 *
 *   The plugin exposes `virtual:stackra-routing/dev-subdomain` — a
 *   Vite virtual module the runtime imports to read the current
 *   request's subdomain when `?_subdomain=` overrides land during dev.
 *   The module returns a single `getDevSubdomain()` function that
 *   pulls the value out of `globalThis.__STACKRA_DEV_SUBDOMAIN__` (set
 *   by the dev middleware on each request).
 */

/**
 * The public id consumers write in an `import` statement. Vite hands
 * this to the plugin's `resolveId` hook.
 */
export const VIRTUAL_DEV_SUBDOMAIN_ID = "virtual:stackra-routing/dev-subdomain";

/**
 * The resolved id — Vite convention: prefix the public id with the
 * null-byte marker so the built-in file-system resolver skips it.
 */
export const RESOLVED_DEV_SUBDOMAIN_ID = `\0${VIRTUAL_DEV_SUBDOMAIN_ID}`;

/**
 * Return the source string for the virtual module. Kept as a small
 * pure builder so the plugin's `load()` hook stays trivial + the
 * source stays testable in isolation.
 *
 * @returns Module source code as a string. The module exports a
 *   single `getDevSubdomain()` function.
 */
export function buildDevSubdomainModuleSource(): string {
  // The virtual module reads `globalThis.__STACKRA_DEV_SUBDOMAIN__` —
  // a soft-typed handle the dev middleware writes on every request.
  // We fall through to `null` when the middleware isn't active (e.g.
  // when the caller imports the module in a non-dev build).
  return [
    "/**",
    " * Virtual dev-subdomain bridge — generated by `@stackra/routing/vite`.",
    " * The dev middleware writes the parsed subdomain to",
    " * `globalThis.__STACKRA_DEV_SUBDOMAIN__` before delegating to Vite's",
    " * next middleware; this module surfaces that value to the runtime.",
    " */",
    "export function getDevSubdomain() {",
    '  if (typeof globalThis === "undefined") return null;',
    "  const value = globalThis.__STACKRA_DEV_SUBDOMAIN__;",
    '  return typeof value === "string" ? value : null;',
    "}",
    "",
  ].join("\n");
}
