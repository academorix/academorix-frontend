<?php

declare(strict_types=1);

namespace Stackra\Webhook\Actions\Tenant;

use Stackra\Authorization\Attributes\RequirePermission;
use Stackra\Routing\Attributes\AsAction;
use Stackra\Routing\Attributes\Middleware;
use Stackra\Routing\Attributes\Post;
use Stackra\Routing\Concerns\AsController;
use Stackra\Tenancy\Contracts\Services\TenantContextInterface;
use Stackra\Webhook\Contracts\Data\WebhookSubscriptionInterface;
use Stackra\Webhook\Contracts\Repositories\WebhookSubscriptionRepositoryInterface;
use Stackra\Webhook\Data\Requests\CreateWebhookSubscriptionRequestData;
use Stackra\Webhook\Data\WebhookSubscriptionData;
use Stackra\Webhook\Enums\WebhookPermission;

/**
 * `POST /api/v1/tenant/webhook/subscriptions` — tenant admin creates
 * a webhook subscription.
 *
 * The signing secret is generated by the observer — the response
 * NEVER carries it. Tenants receive the secret through a separate
 * "reveal secret" flow (out of scope for this action).
 *
 * @category Webhook
 *
 * @since    0.1.0
 */
#[AsAction(name: 'webhook.tenant.subscriptions.create')]
#[Post('/api/v1/tenant/webhook/subscriptions')]
#[Middleware(['api', 'resolve.tenant', 'auth:sanctum', 'tenant.user'])]
#[RequirePermission(WebhookPermission::ManageOwn)]
final class CreateSubscription
{
    use AsController;

    public function __construct(
        private readonly WebhookSubscriptionRepositoryInterface $subscriptions,
        private readonly TenantContextInterface $tenantContext,
    ) {
    }

    public function __invoke(CreateWebhookSubscriptionRequestData $data): WebhookSubscriptionData
    {
        $tenant = $this->tenantContext->currentOrFail();

        $subscription = $this->subscriptions->create([
            WebhookSubscriptionInterface::ATTR_TENANT_ID             => (string) $tenant->getKey(),
            WebhookSubscriptionInterface::ATTR_NAME                  => $data->name,
            WebhookSubscriptionInterface::ATTR_DESTINATION           => $data->destination->value,
            WebhookSubscriptionInterface::ATTR_DESTINATION_CONFIG    => $data->destinationConfig,
            WebhookSubscriptionInterface::ATTR_EVENTS                => $data->events,
            WebhookSubscriptionInterface::ATTR_API_VERSION           => $data->apiVersion,
            WebhookSubscriptionInterface::ATTR_RATE_LIMIT_PER_MINUTE => $data->rateLimitPerMinute,
            WebhookSubscriptionInterface::ATTR_BACKOFF_STRATEGY      => $data->backoffStrategy,
            WebhookSubscriptionInterface::ATTR_BACKOFF_CONFIG        => $data->backoffConfig,
            WebhookSubscriptionInterface::ATTR_HEALTH_PROBE_URL      => $data->healthProbeUrl,
        ]);

        return WebhookSubscriptionData::fromModel($subscription->refresh());
    }
}
